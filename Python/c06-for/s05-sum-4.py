# 반복문(for)
# range(시작, 끝, 간격) 
#   - 시작부터 끝까지 연속적인 숫자를 생성

# 1부터 10까지의 연속된 수에서 홀수만 출력하라.
for cnt in range(1, 11, 2): # 
    print(f"{cnt}", end=' ')


#%%

# 2부터 10까지의 연속된 수에서 짝수만 출력하라.
for cnt in range(2, 11, 2): # 
    print(f"{cnt}", end=' ')


